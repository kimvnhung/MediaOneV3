    private boolean isAvailable(String ten, int soLuong, int giaMua, int giaBan ) {
        boolean result = true;
        if(ten.equals("")){
            notifyError(txtfTenMatHangNhapHang);
            result = false;
        }
        if(soLuong == 0){
            notifyError(txtfSoLuongNhapHang);
            result = false;
        }
        if(soLuong < 0){
            notifyError(txtfSoLuongNhapHang,"Số lượng không thể âm!");
            result = false;
        }
        
        if(giaMua == 0){
            notifyError(txtfGiaMuaNhapHang);
            result = false;
        }
        if(giaMua < 0){
            notifyError(txtfGiaMuaNhapHang,"Giá mua không thể âm!");
            result = false;
        }
        
        if(giaBan == 0){
            notifyError(txtfGiaBanNhapHang);
            result = false;
        }
        if(giaBan < 0){
            notifyError(txtfGiaBanNhapHang,"Giá bán không thể âm!");
            result = false;
        }
        
        return result;
    }
    
    private void notifyError(javax.swing.JTextField smt) {
        smt.setText("Không được để trống");
        smt.setForeground(java.awt.Color.RED);
    }
    
    private void notifyError(javax.swing.JTextField smt,String error) {
        smt.setText(error);
        smt.setForeground(java.awt.Color.RED);
    }
    
    private void handleWhenBeFocus(javax.swing.JTextField smt){
        if(smt.getForeground() == Color.RED){
            smt.setText("");
            smt.setForeground(Color.black);
        }
    }

