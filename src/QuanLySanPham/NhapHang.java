    private void btLuuNhapHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLuuNhapHangActionPerformed
        // TODO add your handling code here:
        try{
            String maMH = txtfMaMHNhapHang.getText().toString();
            String ten = txtfTenMatHangNhapHang.getText().toString();
            String loaiSP = comBLoaiMHNhapHang.getSelectedItem().toString();
            int soLuong = Integer.parseInt(txtfSoLuongNhapHang.getText().toString());
            int giaMua = Integer.parseInt(txtfGiaMuaNhapHang.getText().toString());
            int giaBan = Integer.parseInt(txtfGiaBanNhapHang.getText().toString());
            
            String ref1 = txtfRef1NhapHang.getText().toString();
            String ref2 = txtfRef2NhapHang.getText().toString();
            String ref3 = txtfRef3NhapHang.getText().toString();
            String theLoai = txtfTheLoaiNhapHang.getText().toString();
            if(isAvailable(ten,soLuong,giaMua,giaBan)){
                switch(loaiSP){
                    case "Đĩa Nhạc":
                        DiaNhac diaNhacMoi = new DiaNhac(ten,loaiSP,ref1,ref2,ref3,theLoai,soLuong,giaMua,giaBan);
                        HomeFrame.cuaHang.getListDiaNhac().add(diaNhacMoi);
                        HomeFrame.cuaHang.nhapKhoHang("Đĩa Nhạc");
                        break;
                    case "Đĩa Phim":
                        DiaPhim diaPhimMoi = new DiaPhim(ten,loaiSP,ref1,ref2,ref3,theLoai,soLuong,giaMua,giaBan);
                        HomeFrame.cuaHang.getListDiaPhim().add(diaPhimMoi);
                        HomeFrame.cuaHang.nhapKhoHang("Đĩa Phim");
                        break;
                    default:
                        Sach sachMoi = new Sach(ten,loaiSP,ref1,ref2,ref3,theLoai,soLuong,giaMua,giaBan);
                        HomeFrame.cuaHang.getListSach().add(sachMoi);
                        HomeFrame.cuaHang.nhapKhoHang("Các Loại Sách");
                        break;
                }
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
                setMaMatHang();
                
            }
        }catch (Exception e){
            try{
                int soLuong = Integer.parseInt(txtfSoLuongNhapHang.getText().toString());
            }catch (NumberFormatException e1){
                notifyError(txtfSoLuongNhapHang,"Số lượng phải là số tự nhiên");
            }
            
            
            try{
                int giaMua = Integer.parseInt(txtfGiaMuaNhapHang.getText().toString());
            }catch (NumberFormatException e1){
                notifyError(txtfGiaMuaNhapHang,"Giá mua phải là số tự nhiên");
            }
            
            try{
                int giaBan = Integer.parseInt(txtfGiaBanNhapHang.getText().toString());
            }catch (NumberFormatException e1){
                notifyError(txtfGiaBanNhapHang,"Giá mua phải là số tự nhiên");
            }
            
        }
    }//GEN-LAST:event_btLuuNhapHangActionPerformed

